		public static WasRequest Save(WasRequest request)
		{
			bool isTran = false;

			try
			{
				if (request == null || (request.Data == null && request.SqlId.IsNullOrEmpty()))
					throw new Exception("처리요청이 없습니다.");

				bool isOneRequest = true;
				List<WasRequest> list = new List<WasRequest>();
				if (request.Data != null && request.Data.GetType() == typeof(JArray))
				{
					list = request.Data.JsonToAnyType<List<WasRequest>>();
					isOneRequest = false;
				}
				else
				{
					list.Add(request);
				}

				if (request.IsTransaction)
				{
					DaoFactory.InstanceBiz.BeginTransaction();
					isTran = true;
				}

				try
				{
					//테이블
					if (list.Count > 0)
					{
						foreach (WasRequest req in list)
						{
							if (req.Data == null)
								throw new Exception("저장할 데이터가 존재하지 않습니다.");

							switch (req.ModelName)
							{
								case "BTOrderSumByChannel":
									req.SaveOrderSumByChannel();
									break;
							}
						}
					}

					if (isTran)
						DaoFactory.InstanceBiz.CommitTransaction();
				}
				catch (Exception ex)
				{
					if (isTran)
						DaoFactory.InstanceBiz.RollBackTransaction();

					throw new Exception(ex.Message);
				}

				if (isOneRequest)
				{
					request = list[0];
				}
				else
				{
					request.Data = list;
				}

				return request;
			}
			catch (Exception ex)
			{
				request.Error.Number = ex.HResult;
				request.Error.Message = ex.Message;
				return request;
			}
		}

		public static WasRequest Delete(WasRequest request)
		{
			bool isTran = false;

			try
			{
				if (request == null || (request.Data == null && request.SqlId.IsNullOrEmpty()))
					throw new Exception("처리요청이 없습니다.");

				bool isOneRequest = true;
				List<WasRequest> list = new List<WasRequest>();
				if (request.Data != null && request.Data.GetType() == typeof(JArray))
				{
					list = request.Data.JsonToAnyType<List<WasRequest>>();
					isOneRequest = false;
				}
				else
				{
					list.Add(request);
				}

				if (request.IsTransaction)
				{
					DaoFactory.InstanceBiz.BeginTransaction();
					isTran = true;
				}

				try
				{
					foreach (WasRequest req in list)
					{
						DataMap map = req.Data.JsonToAnyType<DataMap>();
						DaoFactory.InstanceBiz.Delete(req.SqlId, map);
					}

					if (isTran)
						DaoFactory.InstanceBiz.CommitTransaction();
				}
				catch (Exception ex)
				{
					if (isTran)
						DaoFactory.InstanceBiz.RollBackTransaction();

					throw new Exception(ex.Message);
				}

				if (isOneRequest)
				{
					request = list[0];
				}
				else
				{
					request.Data = list;
				}

				return request;
			}
			catch (Exception ex)
			{
				request.Error.Number = ex.HResult;
				request.Error.Message = ex.Message;
				return request;
			}
		}

		private static void SaveOrderSumByChannel(this WasRequest req)
		{
			try
			{
				int count = 0;
				BTOrderSumByChannel model = req.Data.JsonToAnyType<BTOrderSumByChannel>();
				if (model != null)
				{
					if (model.OrderSumList != null && model.OrderSumList.Count > 0)
					{
						foreach (var line in model.OrderSumList)
						{
							line.ChannelID = model.ChannelID;
							line.OrderDate = model.OrderDate;

							DataMap map = new DataMap()
							{
								{ "ChannelID", line.ChannelID },
								{ "BrandID", line.BrandID },
								{ "OrderDate", line.OrderDate }
							};

							var dup = DaoFactory.InstanceBiz.QueryForObject<BTOrderSum>("SelectBTOrderSumDuplicate", map);
							if (dup == null)
							{
								if (line.OrderQty != 0 || line.OrderAmt != 0)
								{
									line.CreateBy = req.User.UserId;
									line.CreateByName = req.User.UserName;

									object id = DaoFactory.InstanceBiz.Insert("InsertBTOrderSum", line);
									line.ID = id.ToIntegerNullToNull();
									count++;
								}
							}
							else
							{
								if (line.OrderQty != dup.OrderQty || line.OrderAmt != dup.OrderAmt)
								{
									if (line.ID.IsNullOrDefault())
										line.ID = dup.ID;

									line.UpdateBy = req.User.UserId;
									line.UpdateByName = req.User.UserName;

									DaoFactory.InstanceBiz.Update("UpdateBTOrderSum", line);
									count++;
								}
							}
						}
					}

					req.Result.Count = count;
					req.Error.Number = 0;
				}
			}
			catch
			{
				throw;
			}
		}
